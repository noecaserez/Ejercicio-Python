# Ejercicio 11: Crear una lista con varios números, 
# luego ordenarlos de manera creciente y devolver por 
# pantalla la lista ordenada y cuál es el menor y cuál el mayor.

#Ejercicio 11

lista_numeros = [4, 2, 7, 12, 10, 9, 3, 6, 8, 5,11]

lista_numeros.sort() #Para ordenarlos de manera creciente.

print(lista_numeros)  #Imprimo por pantalla la lista ordenada.
print(min(lista_numeros))  #Imprimo por pantalla el valor mínimo de la lista.
print(max(lista_numeros)) #Imprimo por pantalla el valor máximo de la lista.