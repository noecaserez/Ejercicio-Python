""" Ejercicio 5: Crear un programa que pida al usuario 
ingresar 2 números por teclado y devuelva por 
pantalla la suma de ellos en un renglón, la resta en otro, 
el producto en otros y la división en otro.

"""

# Ejercicio 5

numero1 = int(input("Ingrese un numero: "))
numero2 = int(input("Ingrese otro numero: "))

print(f"La SUMA es: {numero1 + numero2}")

print(f"La RESTA es: {numero1 - numero2} ")

print(f"El PRODUCTO/MULTIPLICACION es: {numero1 * numero2}")

print(f"La DIVISION es: {numero1 / numero2}")
